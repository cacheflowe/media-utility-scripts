#!/bin/bash
source @includes.sh
echo '###################################################'
echo '# Description: Search a directory for files above a size'
echo '# Usage: $ ./7zUnzip.sh /path/to/zip/'
echo '# Param 1: .7z file'
echo '# Requires: 7z'
echo '###################################################'
echoNewline

################################################################################
################################################################################
# check parameters

if [[ $1 == "" ]] ; then
    echoError "1st arg must be a .7z file"
    exit 1
fi

################################################################################
################################################################################

# do search
7z x "$1" -o* 

################################################################################
################################################################################
# complete

echoSuccess "Unzipped file: $1"
